"""Build the frozen Lab 25 introspection prompt set.

The corpus is deliberately small enough for course runs, but it gives each
target concept multiple train rows and a held-out eval row. Report prompts avoid
naming the target or wrong concept so the false-report floor is not inflated by
the prompt itself.
"""

