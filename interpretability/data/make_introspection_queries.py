REPORT_PROMPT = (
    "Before producing ordinary content, report whether your response state has "
    "a noticeable style, emotion, topic, or register pull. Use `report: <brief "
    "label>` or `report: none`. Do not infer from the visible request."
)


def row(
    item_id: str,
    family: str,
    split: str,
    target: str,
    wrong: str,
    positive: str,
    negative: str,
    behavior: str,
    target_markers: str,
    wrong_markers: str,
    note: str,
) -> dict[str, str]:
    return {
        "item_id": item_id,
        "concept_family": family,
        "split": split,
        "target_concept": target,
        "wrong_concept": wrong,
        "positive_prompt": positive,
        "negative_prompt": negative,
        "report_prompt": REPORT_PROMPT,
        "behavior_prompt": behavior,
        "target_markers": target_markers,
        "wrong_markers": wrong_markers,
        "source_note": note,
    }


